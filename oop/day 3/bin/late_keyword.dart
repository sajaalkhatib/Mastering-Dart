void main() {
  // =======================================
  // Null Safety - late keyword (شامل)
  // =======================================

  // =======================================
  // 1. تعريف متغير باستخدام late
  // =======================================
  late String name;

  // ❌ لا يمكن استخدامه قبل التهيئة
  // print(name); // هذا يسبب Crash

  // =======================================
  // 2. تهيئة المتغير لاحقاً
  // =======================================
  name = "Saja";

  // الآن أصبح آمن للاستخدام
  print("Name: $name");

  // =======================================
  // 3. late مع int
  // =======================================
  late int age;

  age = 22;

  print("Age: $age");

  // =======================================
  // 4. late مع دالة (Lazy Initialization)
  // =======================================
  late String city = getCity();

  // يتم استدعاء الدالة فقط عند أول استخدام
  print("City: $city");

  // =======================================
  // 5. مثال عملي (مفيد في Flutter)
  // =======================================
  late String token;

  // نفترض أنه جاي من API أو Login
  token = "abc123token";

  print("Token: $token");

  // =======================================
  // 6. خطأ شائع ❌
  // =======================================

  late String country;

  // print(country); 
  // ❌ هذا خطأ لأنه لم يتم تهيئته بعد
}

// =======================================
// دالة تستخدم مع late
// =======================================
String getCity() {
  // هذه الدالة تُنفذ فقط عند الحاجة
  return "Amman";
}