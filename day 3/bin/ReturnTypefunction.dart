// =======================================
// Return Type vs Void Function - Dart
// =======================================

void main() {
  // ===================================
  // استدعاء void function
  // ===================================
  sayHello(); // فقط تنفيذ بدون قيمة راجعة

  // ===================================
  // استدعاء function بترجع قيمة
  // ===================================
  int result = add(4, 6); // تخزين القيمة الراجعة
  print("Result: $result");

  // ===================================
  // استخدام return مباشرة
  // ===================================
  print("Multiply: ${multiply(3, 5)}");

  // ===================================
  // تجربة bool return
  // ===================================
  bool isAdultResult = isAdult(20);
  print("Is Adult: $isAdultResult");

  // ===================================
  // تجربة String return
  // ===================================
  String message = greetUser("Saja");
  print(message);
}

// =======================================
// 1. Void Function
// =======================================
void sayHello() {
  // void = ما بترجع أي قيمة
  // فقط تنفيذ أمر
  print("Hello!");
}

// =======================================
// 2. Function with Return Type (int)
// =======================================
int add(int a, int b) {
  // لازم ترجع int لأن نوع الدالة int
  return a + b;
}

// =======================================
// 3. Arrow Function (Return Type)
// =======================================
int multiply(int a, int b) => a * b;
// اختصار لدالة بترجع قيمة بدون استخدام {}

// =======================================
// 4. Return Type (bool)
// =======================================
bool isAdult(int age) {
  // ترجع true إذا العمر >= 18
  return age >= 18;
}

// =======================================
// 5. Return Type (String)
// =======================================
String greetUser(String name) {
  // ترجع نص بدل ما تطبعه
  return "Hello $name";
}

